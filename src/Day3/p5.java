package Day3;

public class p5 {
    private String name;
    private int usn;

    public p5(String name, int usn) {
        this.name = name;
        this.usn = usn;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getUsn() {
        return usn;
    }

    public void setUsn(int usn) {
        this.usn = usn;
    }
}
